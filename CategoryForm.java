    public void populateTable() throws SQLException, ClassNotFoundException {
         CategoryServiceImpl categoryService= new CategoryServiceImpl();
        ArrayList<Category> categoryArrayList=categoryService.categoryArrayList();
        String[] colNames={"id","name","status"};
        Object[][] rows=new Object[categoryArrayList.size()][3];
        for(int i=0;i<categoryArrayList.size();i++){
            rows[i][0]=categoryArrayList.get(i).getID();
            rows[i][1]=categoryArrayList.get(i).getName();
            rows[i][2]=categoryArrayList.get(i).getStatus();
//            Model model= new Model(colNames,rows);
            jTable1.setModel(new Model(colNames,rows));
        }
    }
    public void refreshTable() throws SQLException, ClassNotFoundException {
        jTable1.setModel(new DefaultTableModel());
        populateTable();
    }

