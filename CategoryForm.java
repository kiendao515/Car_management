    private void button2ActionPerformed(java.awt.event.ActionEvent evt) throws SQLException, ClassNotFoundException {
        // TODO add your handling code here:
        TableModel tableModel= jTable1.getModel();
        int rowSelected= jTable1.getSelectedRow();
        int id=Integer.parseInt(tableModel.getValueAt(rowSelected,0).toString());
        int dialogResult=JOptionPane.showConfirmDialog(null,"Do you want to delete the record","warning",0);
        if(dialogResult==0){
            Connection connection=myConnection.connectDb();
            PreparedStatement preparedStatement=connection.prepareStatement("delete from category where id=?");
            preparedStatement.setInt(1,id);
           if(preparedStatement.executeUpdate()!=0){
               JOptionPane.showMessageDialog(null,"Xoa thanh cong !");
           }else{
               JOptionPane.showMessageDialog(null,"xoa that bai~~");
           }
        }
        jTextField2.setText("");
        jTextField2.requestFocus();
        jComboBox1.setSelectedIndex(-1);
        refreshTable();
    }
    // update category
    private void button3ActionPerformed(java.awt.event.ActionEvent evt) throws SQLException, ClassNotFoundException {
        // TODO add your handling code here:
        TableModel defaultTableModel= jTable1.getModel();
        int selectedRow= jTable1.getSelectedRow();
        int id=Integer.parseInt(defaultTableModel.getValueAt(selectedRow,0).toString());
        Connection connection= myConnection.connectDb();
        PreparedStatement preparedStatement= connection.prepareStatement("update category set name=?,status=? where id=?");
        preparedStatement.setString(1,jTextField2.getText());
        preparedStatement.setString(2,jComboBox1.getSelectedItem().toString());
        preparedStatement.setInt(3,id);
        if(preparedStatement.executeUpdate()!=0){
            JOptionPane.showMessageDialog(null,"Update successfully!");
        }else {
            JOptionPane.showMessageDialog(null,"Update fail");
        }
        refreshTable();
        jComboBox1.setSelectedIndex(-1);
        jTextField2.setText("");
        jTextField2.requestFocus();
    }

